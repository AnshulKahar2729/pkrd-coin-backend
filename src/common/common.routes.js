var express = require('express');
var router = express.Router();
require('express-group-routes');
var app = express();

/*
|------------------------------------------------------------------------------------
|  Call routes
|------------------------------------------------------------------------------------
|
*/
var basicRoute = require('./modules/basic/routes');

/*
|--------------------------------------------------------------------------------------
|   Common group
|--------------------------------------------------------------------------------------
|
*/
app.group("/common", (router) => {
    /*
    |-----------------------------------------------------------------------------------
    |   Basic Routes
    |-----------------------------------------------------------------------------------
    |
    */
    router.use(basicRoute)

});

module.exports = app;
